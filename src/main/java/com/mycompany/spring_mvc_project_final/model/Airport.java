package com.mycompany.spring_mvc_project_final.model;

/**
 *
 * @author ADMIN
 */
public class Airport {
    private int id;
    private String name;
    private City city;

    public Airport() {
    }
    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public City getCity() {
        return city;
    }

    public void setCity(City city) {
        this.city = city;
    }
    
}
